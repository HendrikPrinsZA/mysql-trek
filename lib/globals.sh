#-----------------------------------------------------------------------------------------------------------------------
# Cleanup temporary file in case of keyboard interrupt or termination signal.
#-----------------------------------------------------------------------------------------------------------------------
cleanup_temp() {
    # echo "Called 'cleanup_temp'"
    exit 0
    [ -e $tmpfile ] && rm -f $tmpfile
    exit 0
}
trap cleanup_temp SIGHUP SIGINT SIGPIPE SIGTERM
